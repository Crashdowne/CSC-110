
public class nestedLoop {

	public static void main(String[] args) {
		
		//Want to give jellybeans to 3 friends
		for(int friend = 0; friend < 3; friend++) {
			
			System.out.println("Friend number "+friend);
			
			for(int jellybean = 0; jellybean < 5; jellybean++) {
				System.out.print(jellybean);
				
			}
			System.out.println();
			
		}
		
	}
	
}
